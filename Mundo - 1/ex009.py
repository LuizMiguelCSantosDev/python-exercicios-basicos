n= int(input('Digite o valor em metros: '))

print('O valor em quilômetros é: {} km'.format(n/1000))
print('O valor em hectômetros é: {} hm'.format(n/100))
print('O valor em decâmetros é: {} dam'.format(n/10))
print('O valor em decímetros é {} dm'.format(n*10))
print('O valor em centímetros é: {} cm'.format(n * 100))
print('O valor em milímetros é : {} mm'.format(n * 1000))